package com.kunitskaya.domain.module6.abstractbeans;

public class F {
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "F{" +
                "name='" + name + '\'' +
                '}';
    }
}
